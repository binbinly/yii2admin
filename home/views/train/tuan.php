<script type="text/javascript">
    var days = "<?= $data['days']?>";
    var min = "<?= $data['num']?>";
    var max = "<?= $data['max']?>";
    $(function () {
        /* 加减数量 */
        $('.amount').on('click','.jia',function(){
            var ipt = $(this).siblings('input');
            var num = parseInt(ipt.val());
            if(num+1 > max) {
                //layer.msg('人数已满');return ;
            }
            ipt.val(num+1);
            total();//更新总价格
        });
        $('.amount').on('click','.jian',function(){
            var ipt = $(this).siblings('input');
            var num = parseInt(ipt.val());
            if(num-1 < 1) {
                layer.msg('人数不得小于最小值');return ;
            }
            if(num<1){
                return;
            }
            ipt.val(num-1);
            total()//更新总价格
        });
        $(".date_price_content td").click(function(){
            var date_time = $(this).attr("data-time");
            if(date_time == 'ok') {
                layer.msg('当天已成团哦，选择其他日期吧!');
                return ;
            }
            if(date_time) {
                $(".date_price_content td").css('border', '0px');
                $(this).css('border', '1px solid red');
                $(".start-time").val(date_time);
            }
        });

    });
    function total(){
        var t = 0, items = $('.items');
        items.each( function(k, item){
            var time  = parseInt(1);
            var num   = parseInt($(item).find('.amount input').val());
            var price = parseInt($(item).find('.price').text());
            t += time * num * price;
            console.log(price);
        });
        $('#total').html(t);
    }
</script>
